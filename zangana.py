import requests
from termcolor import colored

# فانکشنی پشکنینی بەرزبوونی ڤیدۆ بۆ URL-ەکان
def پشکنینی_لینکی_ڤیدۆ(بەستەر, ئایدی_ڤیدۆ):
    # هاوکردنەوەی بەستەر و ئایدی ڤیدۆ
    بەستەری_تەواو = f"{بەستەر}/{ئایدی_ڤیدۆ}"

    # پشکنینی بەرزبوونی بەستەرەکە
    try:
        وەڵام = requests.head(بەستەری_تەواو)

        # ڕەنگی سەوز بۆ ئامادەبوونی ڤیدۆ، ڕەنگی سور بۆ نەبوونی
        if وەڵام.status_code == 200:
            print(colored(f"ڤیدۆ ئامادەیە: {بەستەری_تەواو}", "green"))
        else:
            print(colored(f"ڤیدۆ ئامادە نیە: {بەستەری_تەواو}", "red"))

    except requests.RequestException as e:
        print(f"هەڵە لە گەیشتن بە بەستەرەکە: {e}")

# فانکشنی خوێندنەوەی ئایدیەکان لە فایلێک
def خوێندنەوەی_ئایدی_لە_فایل(ناوی_فایل):
    with open(ناوی_فایل, "r") as file:
        ئایدیەکان = [x.strip() for x in file.readlines()]
    return ئایدیەکان

# بەستەری سەرەکی خۆت و ناوی فایلەکەی دیاریکراو
بەستەر = "https://roshnaiy.cdn.spotlightr.com/watch"  # URLی سەرەکی تاقیکردنەوە
ناوی_فایل = "video_ids.txt"  # فایلێکی .txt کە تێدا هەر ئایدی ڤیدۆیەک دانراوە

# خوێندنەوەی ئایدیەکان و پشکنینی هەر یەک
ئایدیەکان = خوێندنەوەی_ئایدی_لە_فایل(ناوی_فایل)
for ئایدی in ئایدیەکان:
    پشکنینی_لینکی_ڤیدۆ(بەستەر, ئایدی)
